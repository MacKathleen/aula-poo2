    private void btInsImgMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btInsImgMouseClicked
        JFileChooser exploradorArquivos = new JFileChooser();

        exploradorArquivos.setDialogTitle("Escolha um arquivo");

        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Imagens", "jpg", "jpeg", "png");
        exploradorArquivos.setFileFilter(filtro);

        exploradorArquivos.setMultiSelectionEnabled(false);

        int resultado = exploradorArquivos.showOpenDialog(null);

        if (resultado == JFileChooser.APPROVE_OPTION) {
            
            File arquivo = exploradorArquivos.getSelectedFile();
            
            Icon icone = Utils.converterFileParaIcon(arquivo);
            
            ImageIcon iconeRedimensionado = Utils.redimensionar(icone, 90, 90);
            
            lblFoto.setIcon(iconeRedimensionado);
        }


    }//GEN-LAST:event_btInsImgMouseClicked

    public boolean verificarCampos() {
        //verifica√ß√µes
        if (txtNome.equals("")) {
            JOptionPane.showMessageDialog(null, "o campo'Nome' esta em branco!");
            return false;
        }

        if (!txtNome.getText().matches("^[\\p{L} ]+$")) {
            JOptionPane.showMessageDialog(null, "O campo 'nome' possui caracteres inv√°lidos");
            return false;
        }

        if (txtEmail.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "o campo 'Email' esta em branco!");
            return false;
        }

        if (!txtEmail.getText().matches("^[a-z0-9._-]+@[a-z._]+.[a-z._]+$")) {
            JOptionPane.showMessageDialog(null, "O campo 'Email' possui caracteres inv√°lidos");
            return false;

        }

        if (!txtDataNasc.getText().matches("^[0-9]{2}/[0-9]{2}/[0-9]{4}$")) {
            JOptionPane.showMessageDialog(null, "O campo 'Data de Nascimento' possui formanto inv√°lido" + "Ex:01/08/2005");
            return false;
        }

        String senha = new String(txtSenha.getPassword());

        if (senha.length() < 8) {
            JOptionPane.showMessageDialog(null, "O campo 'senha' deve ser maior que 8 caracteres");
            return false;
        }

        if (!senha.equals(new String(txtConfSenha.getPassword()))) {
            JOptionPane.showMessageDialog(null,
                    "As senhas n√£o s√£o iguais");
            return false;
        }

        return true;

    }

    public void gravar() {

        Usuario usu = new Usuario();

        usu.setNome(txtNome.getText());
        usu.setEmail(txtEmail.getText());

        String senha = new String(txtSenha.getPassword());
        String senhaHash = Utils.calcularSHA1(senha);
        usu.setSenha(senhaHash);

        Date data = Utils.converterStringToDate(txtDataNasc.getText());
        usu.setDataNasc(data);

        usu.setAtivo(CheckAtivo.isSelected());

        UsuarioController controller = new UsuarioController();

        if (controller.inserirUsuario(usu)) {
            JOptionPane.showMessageDialog(null, "Usu√°rio: " + usu.getNome()
                    + "Inserido com sucesso!!");

            this.dispose();

        } else {
            JOptionPane.showMessageDialog(null, "Usu√°rio n√£o ser√° inserido!");
        }

    }

