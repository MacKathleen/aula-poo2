    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        // TODO add your handling code here:
        if (gridUsuarios.getSelectedRow() != -1) {
            int posicaoSelecionada = gridUsuarios.getSelectedRow();
            String textoCelula = gridUsuarios.getValueAt(
                    posicaoSelecionada, 0).toString();

            int id_usuarios = Integer.parseInt(textoCelula);

            UsuarioController controller = new UsuarioController();
            if (controller.excluir(id_usuarios) == true) {
                JOptionPane.showMessageDialog(null, "Usuario excluido com sucesso!");
            }

        }
    }//GEN-LAST:event_btExcluirActionPerformed

